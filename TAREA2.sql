/*EJERCICIO #5*/

DECLARE
    SAL_MAX NUMBER;
    SAL_MIN NUMBER;
    DIFERENCIA NUMBER;
BEGIN
    DBMS_OUTPUT.put_line('--------EJERCICIO 5--------');
    SELECT MAX(SALARY) INTO SAL_MAX FROM EMPLOYEES;
    SELECT MIN(SALARY) INTO SAL_MIN FROM EMPLOYEES;
    DIFERENCIA:= SAL_MAX-SAL_MIN;
    DBMS_OUTPUT.put_line(CONCAT('EL SALARIO MAXIMO ES:',SAL_MAX));
    DBMS_OUTPUT.put_line(CONCAT('EL SALARIO MINIMO ES:',SAL_MIN));
    DBMS_OUTPUT.put_line(CONCAT('LA DIFERENCIA ES    :',DIFERENCIA));
END;


    