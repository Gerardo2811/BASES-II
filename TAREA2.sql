/*EJERCICIO #4*/

DECLARE
    DEPT DEPARTMENTS.DEPARTMENT_ID%TYPE;
    DEPTN VARCHAR2(100);
    NUMEMPL NUMBER;
BEGIN
    DBMS_OUTPUT.put_line('------EJERCICIO 4-------');
    DEPT:= 10;
    SELECT DEPARTMENT_NAME INTO DEPTN FROM departments WHERE department_id=DEPT;
    SELECT COUNT(EMPLOYEE_ID) INTO NUMEMPL FROM employees WHERE department_id=DEPT GROUP BY department_id;
    DBMS_OUTPUT.put_line(CONCAT(CONCAT(DEPTN,' '),NUMEMPL));
END;

